    var selectedStation by remember { mutableStateOf<Station?>(null) }

    Scaffold(
        topBar = {
            TopAppBar(
                title = { Text(if (selectedStation == null) "рж░рзЗрж▓ржУрзЯрзЗ рж╕рзНржЯрзЗрж╢ржирж╕ржорзВрж╣" else selectedStation!!.name) },
                navigationIcon = {
                    if (selectedStation != null) {
                        IconButton(onClick = { selectedStation = null }) {
                            Icon(Icons.Filled.ArrowBack, contentDescription = "Back")
                        }
                    }
                }
            )
        }
    ) { padding ->
        if (selectedStation == null) {
            LazyVerticalGrid(
                columns = GridCells.Fixed(2),
                modifier = Modifier
                    .padding(padding)
                    .padding(8.dp)
            ) {
                items(stations) { station ->
                    StationCard(station = station) {
                        selectedStation = station
                    }
                }
            }
        } else {
            StationDetail(station = selectedStation!!, modifier = Modifier.padding(padding))
        }
    }
}

